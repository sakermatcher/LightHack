def suma_lista(lista):
    return sum(lista)

def main():
    print("Ejercicio 1: Suma de elementos de una lista")
    print(suma_lista([1, 2, 3, 4, 5]))    
    print(suma_lista([10, -5, 20]))        
    print(suma_lista([0, 0, 0, 7]))        

if __name__ == "__main__":
    main()